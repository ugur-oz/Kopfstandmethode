package com.ugur.Kopfstandmethode;

public class Losung {
    private String losung;

    public String getLosung() {
        return losung;
    }

    public void setLosung(String losung) {
        this.losung = losung;
    }

    @Override
    public String toString() {
        return "Losung{" +
                "losung='" + losung + '\'' +
                '}';
    }
}
